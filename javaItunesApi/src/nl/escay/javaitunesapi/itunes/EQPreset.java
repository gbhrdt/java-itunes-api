package nl.escay.javaitunesapi.itunes;

/**
 * Equalizer preset configuration
 */
public class EQPreset extends Item {
//	contained by application.
//	band 1 (real) : the equalizer 32 Hz band level (-12.0 dB to +12.0 dB)
//	band 2 (real) : the equalizer 64 Hz band level (-12.0 dB to +12.0 dB)
//	band 3 (real) : the equalizer 125 Hz band level (-12.0 dB to +12.0 dB)
//	band 4 (real) : the equalizer 250 Hz band level (-12.0 dB to +12.0 dB)
//	band 5 (real) : the equalizer 500 Hz band level (-12.0 dB to +12.0 dB)
//	band 6 (real) : the equalizer 1 kHz band level (-12.0 dB to +12.0 dB)
//	band 7 (real) : the equalizer 2 kHz band level (-12.0 dB to +12.0 dB)
//	band 8 (real) : the equalizer 4 kHz band level (-12.0 dB to +12.0 dB)
//	band 9 (real) : the equalizer 8 kHz band level (-12.0 dB to +12.0 dB)
//	band 10 (real) : the equalizer 16 kHz band level (-12.0 dB to +12.0 dB)
//	modifiable (boolean, r/o) : can this preset be modified?
//	preamp (real) : the equalizer preamp level (-12.0 dB to +12.0 dB)
//	update tracks (boolean) : should tracks which refer to this preset be updated when the preset is renamed or deleted?
}
